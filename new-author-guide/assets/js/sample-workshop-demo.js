      downloadButton.disabled = true;
      renderPreview();
      updateStatus(
        assessmentReady()
          ? "Conversation and rapid assessment complete. Generate the clinician handoff."
          : "Conversation complete. Add the quick assessment to generate the clinician handoff.",
        state.route.tone
      );
      input.placeholder = "Optional note for the clinician";
