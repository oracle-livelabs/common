  function explainSql(sql) {
    var upper = String(sql || "").toUpperCase();
    var notes = [];

    if (!String(sql || "").trim()) {
      return "Load a preset or paste a statement, then use Explain. This demo never runs SQL.";
    }

    if (/\b(DROP|TRUNCATE|DELETE|UPDATE|MERGE|ALTER SYSTEM|GRANT DBA|EXECUTE IMMEDIATE)\b/.test(upper)) {
      return [
        "Blocked in demo mode.",
        "",
        "Use the playground to explain safe setup and retrieval statements only.",
        "Allowed patterns in this demo:",
        "- intake and handoff DDL",
        "- guidance chunk and vector index DDL",
        "- retrieval queries using VECTOR_DISTANCE"
      ].join("\n");
    }

    if (/CREATE TABLE\s+INTAKE_SESSION/.test(upper) && /CREATE TABLE\s+INTAKE_MESSAGE/.test(upper)) {
      notes.push("Psychiatry intake schema detected.");
      notes.push("- Creates a session header for the pre-visit intake conversation.");
      notes.push("- Creates the message log that later becomes transcript highlights and report summary.");
      notes.push("- In the next step, add rapid_assessment and clinician_handoff tables.");
      return notes.join("\n");
    }

    if (/CREATE TABLE\s+GUIDANCE_CHUNKS/.test(upper) && /CREATE VECTOR INDEX/.test(upper)) {
      notes.push("Grounded knowledge layer detected.");
      notes.push("- Creates the approved guidance chunk store used by the RAG assistant.");
      notes.push("- Builds the vector index that powers semantic retrieval.");
      notes.push("- In a real workshop, the next step would be loading approved psychiatry guidance and generating embeddings.");
      return notes.join("\n");
    }

    if (/VECTOR_DISTANCE/.test(upper) || /<=>/.test(upper)) {
      notes.push("Retrieval query detected.");
      notes.push("- Finds the closest approved guidance chunks for the current intake question.");
      notes.push("- Keeps the assistant grounded on safety, CBT note, and care-context guidance.");
      notes.push("- Demo result: psychiatry safety escalation playbook, CBT thought and behavior template, medication and substance checklist.");
      return notes.join("\n");
    }

    return [
      "Recognized as safe read-or-build SQL, but this demo does not execute statements.",
      "",
      "What to do next in a real workshop:",
      "- confirm the object owner and target schema",
      "- explain why the object exists",
      "- show one validation query or expected output",
      "- move to the next task only after the learner checks the result"
    ].join("\n");
  }

