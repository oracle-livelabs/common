  function setupScrollSpy() {
    var tracked = Array.prototype.slice.call(document.querySelectorAll("[data-track-section]"));
    var links = Array.prototype.slice.call(document.querySelectorAll("[data-section-link]"));
    var ticking = false;

    function updateActiveState() {
      var marker = window.scrollY + (window.innerHeight * 0.28);
      var activeId = tracked.length ? tracked[0].id : "";

      tracked.forEach(function (section) {
        if (marker >= section.offsetTop) {
          activeId = section.id;
        }
      });

      links.forEach(function (link) {
        link.classList.toggle("is-active", link.getAttribute("data-section-link") === activeId);
      });

      ticking = false;
    }

    function requestUpdate() {
      if (ticking) {
        return;
      }

      ticking = true;
      window.requestAnimationFrame(updateActiveState);
    }

    if (!tracked.length || !links.length) {
      return;
    }

    window.addEventListener("scroll", requestUpdate, { passive: true });
    window.addEventListener("resize", requestUpdate);
    requestUpdate();
  }

